#pragma once
#include "common.h"

class BoxClass
{
private:
	//メンバ変数

	bool m_Flag;	//フラグ
	POSITION m_Position;	//中心座標
	VELOCITY m_Velocity;	//速度
	ACCELARATION m_Accelaration;	//加速度
	VARIABLE_VERTEX m_Vertex;	//頂点
	double m_SemiLongAxis;	//半長軸の長さ
	double m_SemiShortAxis;	//半短軸の長さ
	THREE_DIMENSION_VECTOR m_SemiLongVector;	//半長軸ベクトル
	THREE_DIMENSION_VECTOR m_SemiShortVector;	//半短軸ベクトル

public:
	//コンストラクタ
	BoxClass(void);

	//デストラクタ
	~BoxClass(void);

	//メンバ関数

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	矩形の中心座標を取得する
	//戻り値:
	//	m_Position:矩形の中心座標
	//////////////////////////////////////////////////////////////////////////////
	POSITION GetPosition();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	矩形の4つの頂点を取得する
	//戻り値:
	//	m_Vertex:矩形の4つの頂点
	//////////////////////////////////////////////////////////////////////////////
	VARIABLE_VERTEX GetVertex();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	矩形の半長軸の長さを取得する
	//戻り値:
	//	m_SemiLongAxis:矩形の半長軸の長さ
	//////////////////////////////////////////////////////////////////////////////
	double GetSemiLongAxis();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	矩形の半短軸の長さを取得する
	//戻り値:
	//	m_SemiShortAxis:矩形の半短軸の長さ
	//////////////////////////////////////////////////////////////////////////////
	double GetSemiShortAxis();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	矩形の半長軸のベクトルを取得する
	//戻り値:
	//	m_SemiLongVector:矩形の半長軸のベクトル
	//////////////////////////////////////////////////////////////////////////////
	THREE_DIMENSION_VECTOR GetSemiLongVector();

	//////////////////////////////////////////////////////////////////////////////
	//概略:
	//	矩形の半短軸のベクトルを取得する
	//戻り値:
	//	m_SemiShortVector:矩形の半短軸のベクトル
	//////////////////////////////////////////////////////////////////////////////
	THREE_DIMENSION_VECTOR GetSemiShortVector();
};

