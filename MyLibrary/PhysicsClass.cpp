const double PhysicsClass::m_GravityAccelaration = 1;

//コンストラクタ
PhysicsClass::PhysicsClass(void)
{
	m_GravityVelocity = c_ZeroVector;
	m_GroundedFlag = false;
}

//デストラクタ
PhysicsClass::~PhysicsClass(void)
{
}

